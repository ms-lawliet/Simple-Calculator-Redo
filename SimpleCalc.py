class SimpleCalculator:
    # perform operation
    # for addition,
    def add(self, num1, num2):
        sum = num1 + num2
        return sum

    # for subtraction,
    def subtract(self, num1, num2):
        difference = num1 - num2
        return difference

    # for multiplication,
    def multiply(self, num1, num2):
        product = num1 * num2
        return product

    # for division,
    def divide(self, num1, num2):
        quotient = num1 / num2
        return quotient
